    private void Addch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Addch1ActionPerformed
        String chapterNo = JOptionPane.showInputDialog(this, "Enter Chapter Number:");
if (chapterNo == null || chapterNo.trim().isEmpty()) return;

String chapterTitle = JOptionPane.showInputDialog(this, "Enter Chapter Title:");
if (chapterTitle == null || chapterTitle.trim().isEmpty()) return;

int lastFree = JOptionPane.showConfirmDialog(this,
    "Is this the last free chapter?", "Last Free",
    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION ? 1 : 0;

try (java.sql.Connection conn = config.connectDB();
     java.sql.PreparedStatement pst = conn.prepareStatement(
         "INSERT INTO tbl_chapters (m_id, chapter_no, chapter_title, last_free) VALUES (?,?,?,?)")) {

    pst.setInt(1, manwhaId);
    pst.setString(2, chapterNo.trim());
    pst.setString(3, chapterTitle.trim());
    pst.setInt(4, lastFree);
    pst.executeUpdate();

    JOptionPane.showMessageDialog(this, "Chapter added!");
    loadChapters();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_Addch1ActionPerformed

