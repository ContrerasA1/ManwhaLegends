    private void managepageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_managepageActionPerformed
int row = ManageChaptersTable.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a chapter first!");
    return;
}

String chapterNo = ManageChaptersTable.getValueAt(row, 0).toString();
String chapterTitle = ManageChaptersTable.getValueAt(row, 1).toString();

try (java.sql.Connection conn = config.connectDB();
     java.sql.PreparedStatement pst = conn.prepareStatement(
         "SELECT chapter_id FROM tbl_chapters WHERE m_id=? AND chapter_no=?")) {

    pst.setInt(1, manwhaId);
    pst.setString(2, chapterNo);
    java.sql.ResultSet rs = pst.executeQuery();

    if (rs.next()) {
        int chapterId = rs.getInt("chapter_id");
        new ManagePages(chapterId, chapterTitle).setVisible(true);
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}
    }//GEN-LAST:event_managepageActionPerformed

    private void DeletechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletechActionPerformed
     int row = ManageChaptersTable.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a chapter first!");
    return;
}

int confirm = JOptionPane.showConfirmDialog(this,
    "Delete this chapter and all its pages?", "Confirm",
    JOptionPane.YES_NO_OPTION);

if (confirm == JOptionPane.YES_OPTION) {
    String chapterNo = ManageChaptersTable.getValueAt(row, 0).toString();
    
    try (java.sql.Connection conn = config.connectDB()) {
        java.sql.PreparedStatement getChapter = conn.prepareStatement(
            "SELECT chapter_id FROM tbl_chapters WHERE m_id=? AND chapter_no=?");
        getChapter.setInt(1, manwhaId);
        getChapter.setString(2, chapterNo);
        java.sql.ResultSet rs = getChapter.executeQuery();
        
        if (rs.next()) {
            int chapterId = rs.getInt("chapter_id");
            
            java.sql.PreparedStatement delPages = conn.prepareStatement(
                "DELETE FROM tbl_pages WHERE chapter_id=?");
            delPages.setInt(1, chapterId);
            delPages.executeUpdate();
            
            java.sql.PreparedStatement delChapter = conn.prepareStatement(
                "DELETE FROM tbl_chapters WHERE chapter_id=?");
            delChapter.setInt(1, chapterId);
            delChapter.executeUpdate();
        }
        
        JOptionPane.showMessageDialog(this, "Chapter deleted!");
        loadChapters();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    }//GEN-LAST:event_DeletechActionPerformed

