   private void loadChapters() {
    try (java.sql.Connection conn = config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "SELECT chapter_no, chapter_title, last_free FROM tbl_chapters WHERE m_id=? ORDER BY CAST(chapter_no AS INTEGER)")) {

        pst.setInt(1, manwhaId);
        java.sql.ResultSet rs = pst.executeQuery();

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"Chapter No", "Title", "Last Free"}, 0
        );

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("chapter_no"),
                rs.getString("chapter_title"),
                rs.getInt("last_free") == 1 ? "Yes" : "No"
            });
        }

        ManageChaptersTable.setModel(model);
        ManageChaptersTable.setDefaultEditor(Object.class, null);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

