    private void loadPages() {
    try (java.sql.Connection conn = config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "SELECT page_no, image FROM tbl_pages WHERE chapter_id=? ORDER BY CAST(page_no AS INTEGER)")) {

        pst.setInt(1, chapterId);
        java.sql.ResultSet rs = pst.executeQuery();

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"Page No", "Image Path"}, 0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("page_no"),
                rs.getString("image")
            });
        }
        ManagePageTable.setModel(model);
        ManagePageTable.setDefaultEditor(Object.class, null);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}


