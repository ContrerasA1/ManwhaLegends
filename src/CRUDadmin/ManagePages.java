    private void DeletepgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletepgActionPerformed
      int row = ManagePageTable.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a page first!");
    return;
}

int confirm = JOptionPane.showConfirmDialog(this,
    "Delete this page?", "Confirm", JOptionPane.YES_NO_OPTION);

if (confirm == JOptionPane.YES_OPTION) {
    String pageNo = ManagePageTable.getValueAt(row, 0).toString();
    
    try (java.sql.Connection conn = config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "DELETE FROM tbl_pages WHERE chapter_id=? AND page_no=?")) {
        pst.setInt(1, chapterId);
        pst.setString(2, pageNo);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Page deleted!");
        loadPages();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    }//GEN-LAST:event_DeletepgActionPerformed

    private void AddpgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddpgActionPerformed
      JFileChooser chooser = new JFileChooser();
chooser.setMultiSelectionEnabled(true);
int result = chooser.showOpenDialog(this);

if (result == JFileChooser.APPROVE_OPTION) {
    java.io.File[] files = chooser.getSelectedFiles();
    
    // get current max page no
    int maxPage = 0;
    try (java.sql.Connection conn = config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "SELECT MAX(CAST(page_no AS INTEGER)) FROM tbl_pages WHERE chapter_id=?")) {
        pst.setInt(1, chapterId);
        java.sql.ResultSet rs = pst.executeQuery();
        if (rs.next() && rs.getString(1) != null) maxPage = rs.getInt(1);
    } catch (Exception e) { }

    try (java.sql.Connection conn = config.connectDB();
         java.sql.PreparedStatement pst = conn.prepareStatement(
             "INSERT INTO tbl_pages (chapter_id, page_no, image) VALUES (?,?,?)")) {

        for (int i = 0; i < files.length; i++) {
            pst.setInt(1, chapterId);
            pst.setString(2, String.valueOf(maxPage + i + 1));
            pst.setString(3, files[i].getAbsolutePath());
            pst.executeUpdate();
        }
        JOptionPane.showMessageDialog(this, files.length + " page/s added!");
        loadPages();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    }//GEN-LAST:event_AddpgActionPerformed

